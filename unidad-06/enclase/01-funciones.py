def sumar_dos_numeros( nro_uno = 0, nro_dos = 0):
    """
    Esta función recibe dos números y retorna la suma de ellos
    param nro_uno: int
    param nro_dos: int
    return: int
    """
    if nro_dos == 0:
        print( "No se ha ingresado el segundo número" )
        return "Error"
    # inicio del blque de la función
    total = nro_uno + nro_dos
    return total















