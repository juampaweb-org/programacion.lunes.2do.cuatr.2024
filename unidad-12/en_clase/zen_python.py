




# import this: "El Zen de Python", una serie de principios de diseño.
# PEP-8: es una guía de estilo para escribir código Python limpio y legible.



# Hermoso es mejor que feo
# El código debería ser estéticamente agradable, porque es más fácil de entender y mantener.


# Explícito es mejor que implícito
# Es mejor que las intenciones del código sean claras y no requieran suposiciones.


# Simple es mejor que complejo
# La simplicidad reduce los errores y hace que el código sea más fácil de seguir.


# Complejo es mejor que complicado
# La complejidad es necesaria en algunos casos, pero debe ser manejable, no caótica.


# Plano es mejor que anidado
# Las estructuras de código simples y no demasiado anidadas son más fáciles de entender.


# Disperso es mejor que denso
#  El código debe estar bien distribuido y no amontonado, permitiendo mayor legibilidad.


# La legibilidad cuenta
# El código debe ser fácil de leer, lo que facilita su comprensión y mantenimiento.
# Los casos especiales no son lo suficientemente especiales como para romper las reglas


# Las excepciones no deben alterar el diseño general del código.
# Aunque la practicidad supera a la pureza
# Si bien el código debe seguir las reglas, a veces es más práctico hacer concesiones por el bien del resultado.


# Los errores nunca deberían pasar en silencio
# Los errores deben ser detectados y corregidos, no ignorados.


# A menos que sean explícitamente silenciados
# En situaciones específicas, puede ser aceptable ignorar un error, pero solo de forma consciente.


# Ante la ambigüedad, rechaza la tentación de adivinar
# Si algo no está claro, es mejor no hacer suposiciones. En su lugar, busca claridad.


# Debería haber una —y preferiblemente solo una— forma obvia de hacerlo
# El lenguaje debe favorecer soluciones únicas y claras a los problemas.



# Aunque esa manera puede no ser obvia al principio a menos que seas holandés
# A veces la solución no es evidente, pero se vuelve clara con la experiencia. El comentario sobre ser "holandés" es una referencia humorística a Guido van Rossum, el creador de Python, quien es holandés.



# Ahora es mejor que nunca
# Es mejor actuar y escribir el código ahora que postergarlo indefinidamente.



# Aunque nunca es a menudo mejor que *ahora mismo*
# Sin embargo, apresurarse a implementar algo sin pensarlo bien puede ser perjudicial.




# Si la implementación es difícil de explicar, es una mala idea
# Si no puedes explicar claramente cómo funciona algo, probablemente sea demasiado complicado.



# Si la implementación es fácil de explicar, puede ser una buena idea
# Si puedes explicar una solución de manera sencilla, es probable que sea un buen enfoque.


# Los espacios de nombres son una gran idea — usemos más de ellos
# Usar espacios de nombres (namespaces) evita conflictos en el código y lo organiza mejor.


