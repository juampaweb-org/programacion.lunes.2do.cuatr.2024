

### Ejercicio 1: Leer un archivo de texto e imprimir su contenido
# **Instrucción**: Escribe un programa que lea el contenido de un archivo llamado `texto.txt` e imprima su contenido en la consola.



### Ejercicio 2: Leer un archivo línea por línea e imprimir cada línea
# Escribe un programa que lea un archivo llamado `lineas.txt` e imprima cada línea por separado.



### Ejercicio 3: Contar el número de líneas en un archivo
# Escribe un programa que cuente y muestre el número de líneas de un archivo llamado `lineas.txt`.



### Ejercicio 4: Crear una lista con las palabras de un archivo
# Escribe un programa que lea un archivo llamado `palabras.txt` y cree una lista con todas las palabras del archivo (suponiendo que las palabras están separadas por espacios).




### Ejercicio 5: Escribir una lista de palabras en un archivo
# Escribe un programa que tome una lista de palabras y las escriba en un archivo llamado `salida.txt`, una palabra por línea.




### Ejercicio 6: Leer y escribir archivos (copia de archivo)
# Escribe un programa que lea el contenido de un archivo llamado `origen.txt` y lo copie a un nuevo archivo llamado `copia.txt`.




### Ejercicio 7: Añadir contenido a un archivo existente
# Escribe un programa que añada la frase "Añadiendo esta línea al final del archivo." al final de un archivo llamado `existente.txt`.




### Ejercicio 8: Contar palabras en un archivo
# Escribe un programa que cuente el número de palabras en un archivo llamado `texto.txt`.


### Ejercicio 9: Buscar una palabra específica en un archivo
# Escribe un programa que busque una palabra específica (por ejemplo, "Python") en un archivo llamado `texto.txt` y muestre cuántas veces aparece.


### Ejercicio 10: Crear un archivo con una secuencia de números
# Escribe un programa que cree un archivo llamado `numeros.txt` y escriba los números del 1 al 100, cada número en una nueva línea.

